const add = (i, j) => {
    parseFloat(i + j);
};

const subtract = (i, j) => {
    parseFloat(i - j);
};

const multiply = (i, j) => {
    parseFloat(i * j);
};

const divide = (i, j) => {
    parseFloat(i / j);
};

const Operations = {
    add, subtract, multiply, divide
};

export default Operations